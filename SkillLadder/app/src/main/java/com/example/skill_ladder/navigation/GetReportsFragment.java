        // Inflate the layout for this fragment
                View view =  inflater.inflate(R.layout.fragment_get_reports, container, false);

        ImageView Uesrs = view.findViewById(R.id.Dowmload01);
        Uesrs.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {
//                showCustomToast.showToast(getContext(),"Download Users !",R.drawable.checked);
                generateUserPDF();
            }
        });
